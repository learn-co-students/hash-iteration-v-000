def select_winner(passengers)
  # note: I'm not sure this doesn't seem like best example of when to use .each
  # Since keys are unique, don't we just need to check key? suite_a and if
  # defined, test if passenger name starts with 'A'?
  winner = ""
  passengers.each do |suite, name|
    if suite == :suite_a && name.start_with?("A")
      winner = name
    end
  end
  winner
end
